#include <stdio.h>
#include <windows.h> // Für system("chcp 1252") und system("cls") помогает отображать корректно символы в терминале VsCode.
#pragma execution_character_set("utf-8")// Für system("chcp 1252") und system("cls") помогает отображать корректно символы в терминале VsCode.

int main() {
	system("chcp 65001"); 
	system("cls"); 
	// 1. Создание переменной и присвоение значения
    int zahl = 42; 
    
    // 2. Создание указателя: ptr - это переменная, которая будет хранить адрес.
    // Тип "int *" означает: указатель на int.
    int *ptr; 
    
    // 3. Сохранение адреса 'zahl' в указатель 'ptr'
    // Оператор '&' (Адрес): "дай мне адрес переменной zahl"
    ptr = &zahl; 

    printf("--- Сравнение Прямого Доступа и Доступа Через Указатель ---\n");
    
    // ----------------------------------------------------------------------
    // I. ПРЯМОЙ ДОСТУП К ПЕРЕМЕННОЙ (VALUE)
    // ----------------------------------------------------------------------
    
    // 1. Вывод значения (самого числа)
    printf("1. Прямой доступ (zahl): \t\t\t\t%d\n", zahl); 
    
    // 2. Вывод адреса, где хранится это число
    // Используем '&' для получения адреса. %p - формат для вывода адреса/указателя.
    printf("2. Адрес переменной 'zahl' (&zahl): \t\t\t%p\n", &zahl); 

    printf("------------------------------------------------------------------\n");
    
    // ----------------------------------------------------------------------
    // II. ДОСТУП ЧЕРЕЗ УКАЗАТЕЛЬ (POINTER)
    // ----------------------------------------------------------------------
    
    // 3. Вывод значения, хранящегося в 'ptr' (это адрес!)
    // Вы увидите, что это тот же адрес, что и в пункте 2.
    printf("3. Значение указателя 'ptr' (сам адрес): \t\t%p\n", ptr);
    
    // 4. Вывод значения по адресу, хранящемуся в 'ptr'
    // Оператор '*' (Разыменование): "пойди по адресу, который хранится в ptr, и дай мне значение там"
    printf("4. Значение по адресу (*ptr): \t\t\t\t%d\n", *ptr); 
    
    printf("------------------------------------------------------------------\n");
    
    // Вы можете убедиться, что *ptr и zahl - это одно и то же:
    *ptr = 99; // Меняем значение через указатель
    printf("5. Новое значение 'zahl' после *ptr = 99: \t\t%d\n", zahl);


	return 0;
} 
